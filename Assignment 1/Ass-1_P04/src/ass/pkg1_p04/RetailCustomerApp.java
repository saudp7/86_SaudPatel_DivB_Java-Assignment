    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to delete this record?",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            ps = con.prepareStatement(
                    "DELETE FROM customers WHERE name = ?"
            );
            ps.setString(1, txtName.getText());

            int rows = ps.executeUpdate();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Record Deleted");
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Record Not Found");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        refreshResultSet();

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAverageIncomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAverageIncomeActionPerformed

        try {
            // Call stored procedure
            CallableStatement cs
                    = con.prepareCall("{CALL getAvgIncomeOfMarried(?)}");

            // Register OUT parameter
            cs.registerOutParameter(1, Types.DOUBLE);

            // Execute procedure
            cs.execute();

            // Get result
            double avgIncome = cs.getDouble(1);

            // Handle NULL (no married customers)
            if (cs.wasNull()) {
                JOptionPane.showMessageDialog(
                        this,
                        "No married customers found",
                        "Average Income",
                        JOptionPane.INFORMATION_MESSAGE
                );
            } else {
                JOptionPane.showMessageDialog(
                        this,
                        "Average Annual Income of Married Customers:\n₹ " + avgIncome,
                        "Average Income",
                        JOptionPane.INFORMATION_MESSAGE
                );
            }

            cs.close();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(
                    this,
                    "Error calling procedure:\n" + ex.getMessage(),
                    "Database Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnAverageIncomeActionPerformed

