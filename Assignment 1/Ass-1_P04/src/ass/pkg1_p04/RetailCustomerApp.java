    private void fillFormFromResultSet(ResultSet rs) throws Exception {

        txtName.setText(rs.getString("name"));
        txtHomeAdd.setText(rs.getString("home_address"));
        txtHomePhNo.setText(rs.getString("home_phone"));
        txtBusinessAdd.setText(rs.getString("business_address"));
        txtBusinessPhNo.setText(rs.getString("business_phone"));
        txtFax.setText(rs.getString("fax"));
        txtMobileNo.setText(rs.getString("mobile"));
        txtPager.setText(rs.getString("pager"));
        selectMaritalStatus.setSelectedItem(rs.getString("marital_status"));
        txtNoOfChildren.setText(rs.getString("no_of_children"));
        txtAnnualIncome.setText(rs.getString("annual_income"));

        byte[] img = rs.getBytes("photo");
        if (img != null) {
            ImageIcon icon = new ImageIcon(img);
            Image scaled = icon.getImage().getScaledInstance(
                    lblPhoto.getWidth(),
                    lblPhoto.getHeight(),
                    Image.SCALE_SMOOTH
            );
            lblPhoto.setIcon(new ImageIcon(scaled));
            photoBytes = img;
        }
    }

    private void clearForm() {
        txtName.setText("");
        txtHomeAdd.setText("");
        txtHomePhNo.setText("");
        txtBusinessAdd.setText("");
        txtBusinessPhNo.setText("");
        txtFax.setText("");
        txtMobileNo.setText("");
        txtPager.setText("");
        txtNoOfChildren.setText("");
        txtAnnualIncome.setText("");
        lblPhoto.setIcon(null);
        photoBytes = null;
    }

    private void refreshResultSet() {

        try {
            ps = con.prepareStatement(
                    "SELECT * FROM customers",
                    ResultSet.TYPE_SCROLL_INSENSITIVE,
                    ResultSet.CONCUR_READ_ONLY
            );
            rs = ps.executeQuery();

            if (rs.next()) {
                fillFormFromResultSet(rs);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

